    [InlineData(HeaderOffset_iOS,
        new byte[] { 0x7D, 0x7B, 0xA7, 0x88, 0x88, 0x88, 0x88, 0x88, 0x88, 0x82 }, // MK4 base datagram
        20, // expected size
        new byte[] { 0xF9, 0x08, 0x49, 0x22, 0x47, 0xBA, 0xC4, 0xBC, 0x13, 0xEF, 0x19, 0x82, 0x65, 0xB1, 0xBD, 0x0B, 0x25, 0x09, 0x94, 0xD0, 0x00, 0x00, 0x00, 0x00 })]
    [InlineData(HeaderOffset_Android,
        new byte[] { 0xAD, 0x7B, 0xA7, 0x80, 0x80, 0x80, 0x4F, 0x52 }, // MK4 connect datagram
        18, // expected size
        new byte[] { 0x6D, 0xB6, 0x43, 0xCF, 0x7E, 0x8F, 0x47, 0x11, 0x48, 0x66, 0x59, 0x38, 0xD1, 0x7A, 0x65, 0xE6, 0x34, 0x7F, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00 })]
    [InlineData(HeaderOffset_Android,
        new byte[] { 0x7D, 0x7B, 0xA7, 0x88, 0x88, 0x88, 0x88, 0x88, 0x88, 0x82 }, // MK4 base datagram
        20, // expected size
        new byte[] { 0x6D, 0xB6, 0x43, 0xCF, 0x7E, 0x8F, 0x47, 0x11, 0x98, 0x66, 0x59, 0x30, 0xD9, 0x72, 0xA2, 0x3C, 0x6F, 0x56, 0x11, 0x26, 0x00, 0x00, 0x00, 0x00 })]
    public void GetRfPayload_MouldKing_ShouldReturnExpectedByteArray(int headerOffset, byte[] input, int expectedSize, byte[] expected)
    {
        int payloadLength = expected.Length;
        byte[] rfPayload = new byte[payloadLength];

        int size = CryptTools.GetRfPayload(MKProtocol.SeedArray, MKProtocol.HeaderArray, input, headerOffset, MKProtocol.CTXValue1, MKProtocol.CTXValue2, rfPayload);

        size.Should().Be(expectedSize);
        rfPayload.Should().BeEquivalentTo(expected);

        byte[] decrypt = DecryptTools.DecryptRfPayload(MKProtocol.SeedArray, MKProtocol.HeaderArray.Length, input.Length, headerOffset, MKProtocol.CTXValue1, MKProtocol.CTXValue2, rfPayload);
        decrypt.Should().BeEquivalentTo(input);
    }
}
