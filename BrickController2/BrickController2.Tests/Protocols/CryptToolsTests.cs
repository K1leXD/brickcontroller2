    public void GetRfPayload_CaDA_ShouldReturnExpectedByteArray(int headerOffset, byte[] input, int expectedSize, byte[] expected)
    {
        int payloadLength = expected.Length;
        byte[] rfPayload = new byte[payloadLength];

        int size = CryptTools.GetRfPayload(CaDAProtocol.SeedArray, CaDAProtocol.HeaderArray, input, headerOffset, CaDAProtocol.CTXValue1, CaDAProtocol.CTXValue2, rfPayload);

        size.Should().Be(expectedSize);
        rfPayload.Should().BeEquivalentTo(expected);
    }

