using Xunit;
using FluentAssertions;
using BrickController2.Protocols;
using BrickController2.Tools.Protocols;

namespace BrickController2.Tests.Protocols;

public class CryptToolsTests
{
    private const int HeaderOffset_iOS = 13;
    private const int HeaderOffset_Android = 15;

    [Theory]
    [InlineData(HeaderOffset_iOS,
        new byte[] { 0x75, 0x13, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x80, 0x80, 0x00, 0x00, 0x00, 0x00 },
        24, // expected size
        new byte[] { 0xC0, 0x3D, 0xCA, 0x66, 0x6D, 0x32, 0xB2, 0x9E, 0xD2, 0x57, 0xA1, 0x3D, 0xA7, 0x66, 0xB0, 0x75, 0xB1, 0x91, 0x48, 0x96, 0x77, 0xF8, 0xBD, 0x4D, 0x00, 0x00 })]
    [InlineData(HeaderOffset_Android,
        new byte[] { 0x75, 0x13, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x80, 0x80, 0x00, 0x00, 0x00, 0x00 },
        24, // expected size
        new byte[] { 0xEE, 0x1B, 0xC8, 0xAF, 0x9F, 0x3C, 0xCD, 0x42, 0xFA, 0x2A, 0xB4, 0xE7, 0xD4, 0x0C, 0xB6, 0x2E, 0xA6, 0x82, 0xC9, 0xF2, 0x0E, 0x7F, 0x82, 0x23 })]
    public void GetRfPayload_CaDA_ShouldReturnExpectedByteArray(int headerOffset, byte[] input, int expectedSize, byte[] expected)
    {
        int payloadLength = expected.Length;
        byte[] rfPayload = new byte[payloadLength];

        int size = CryptTools.GetRfPayload(CaDAProtocol.SeedArray, CaDAProtocol.HeaderArray, input, headerOffset, CaDAProtocol.CTXValue1, CaDAProtocol.CTXValue2, rfPayload);

        size.Should().Be(expectedSize);
        rfPayload.Should().BeEquivalentTo(expected);

        byte[] decrypt = DecryptTools.DecryptRfPayload(CaDAProtocol.SeedArray, CaDAProtocol.HeaderArray.Length, input.Length, headerOffset, CaDAProtocol.CTXValue1, CaDAProtocol.CTXValue2, rfPayload);
        decrypt.Should().BeEquivalentTo(input);
    }

    [Theory]
    [InlineData(HeaderOffset_iOS,
        new byte[] { 0xAD, 0x7B, 0xA7, 0x80, 0x80, 0x80, 0x4F, 0x52 }, // MK4 connect datagram
